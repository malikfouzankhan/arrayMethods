// unshift() method
// inserts an element before the first element of the array
// NOTE : unshift() is an in-place method which means it will change the original array

let arr = [1,2,3,4,5,6,7,8,9];
console.log(arr.unshift(0)); // This line will insert the element and return the new length of the array
