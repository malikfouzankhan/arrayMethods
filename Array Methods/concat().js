// concat() method
// merges the two arrays into one in the order [arr1, arr2]
// it is different from arr1 + arr2
let arr1 = [1,2,3,4,5];
let arr2 = [6,7,8,9,0];
console.log("This is concatenation:",arr1.concat(arr2));
console.log("\nThis is adding two arrays using '+' operator:\n\n",arr1 + arr2);

let str1 = ["I","am"];
